public class task4 {
    public task4() {
        for (int hour = 0; hour <= 2; hour++) {
            for (int minute = 0; minute < 60; minute++) {
                for (int second = 0; second < 60; second++) {
                    System.out.println(hour + " h " + minute + " min " + second + " sec");
                }
            }
        }
    }
}
